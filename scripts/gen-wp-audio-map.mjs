#!/usr/bin/env node
/**
 * gen-wp-audio-map.mjs
 *
 * Queries the WordPress media API at allhere.org and builds a static map
 * of `localFilename → publicWpUrl` so audioRegistry.ts can resolve remote
 * audio URLs without subfolder structure (WP flattens uploads to YYYY/MM/
 * and sanitizes filenames — spaces become dashes, parens/commas dropped).
 *
 * Output: src/content/wpAudioMap.generated.ts
 *
 * Run with:  node scripts/gen-wp-audio-map.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const AUDIO_ROOT = path.join(ROOT, 'assets', 'audio');
const OUT_FILE = path.join(ROOT, 'src', 'content', 'wpAudioMap.generated.ts');
const WP_API = 'https://allhere.org/wp-json/wp/v2/media';

/** Approximate WordPress filename sanitization to derive a match key. */
function wpSanitize(filename) {
  const stem = filename.replace(/\.[^.]+$/, '');
  return stem
    .replace(/[(),]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
    .toLowerCase();
}

/** Pull every audio item from WP, paginated. */
async function fetchAllAudio() {
  const all = [];
  for (let page = 1; page < 50; page++) {
    const res = await fetch(`${WP_API}?per_page=100&mime_type=audio/mpeg&page=${page}`);
    if (!res.ok) break;
    const items = await res.json();
    if (!Array.isArray(items) || items.length === 0) break;
    all.push(...items);
    if (items.length < 100) break;
  }
  return all;
}

// Filenames that are bundled and should NOT be looked up on WP.
const BUNDLED_BASENAMES = new Set([
  'One minute meditation.mp3',
  'Three minutes meditation.mp3',
  'bell.mp3',
  'bell_short.mp3',
  'tick.mp3',
  // QM3 home rounds (first 3 + first 2 inters)
  'breath7_round01.mp3',
  'breath7_round02.mp3',
  'breath7_round03.mp3',
  'breath7_round01_inter.mp3',
  'breath7_round02_inter.mp3',
]);

function walkMp3s(dir, acc = []) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const entry of entries) {
    if (entry.name === 'excluded' || entry.name === 'Home') continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walkMp3s(full, acc);
    else if (entry.isFile() && entry.name.toLowerCase().endsWith('.mp3')) {
      if (!BUNDLED_BASENAMES.has(entry.name)) acc.push(full);
    }
  }
  return acc;
}

async function main() {
  console.log('Fetching WP audio media…');
  const wpItems = await fetchAllAudio();
  console.log(`  ${wpItems.length} audio items on WP`);

  // Build sanitized-stem → URL map. Prefer the "canonical" upload over its
  // -1 / -2 duplicate (WordPress appends a suffix on re-upload). We pick
  // whichever entry has the SHORTEST sanitized stem (no numeric suffix).
  const slugToUrl = new Map();
  for (const item of wpItems) {
    const url = item.source_url;
    if (!url) continue;
    const fn = decodeURIComponent(url.split('/').pop());
    const sanitized = wpSanitize(fn);
    const canonical = sanitized.replace(/-\d+$/, '');
    if (!slugToUrl.has(canonical) || slugToUrl.get(canonical).length > url.length) {
      // Prefer URL without the -N suffix in the actual filename.
      const existingFn = slugToUrl.has(canonical)
        ? decodeURIComponent(slugToUrl.get(canonical).split('/').pop())
        : null;
      const existingHasSuffix = existingFn && /-\d+\.mp3$/i.test(existingFn);
      const newHasSuffix = /-\d+\.mp3$/i.test(fn);
      if (!slugToUrl.has(canonical) || (existingHasSuffix && !newHasSuffix)) {
        slugToUrl.set(canonical, url);
      }
    }
  }

  console.log(`  ${slugToUrl.size} unique canonical slugs`);

  // For each local mp3 (excluding Home/ which stays bundled), find its WP URL.
  const localMp3s = walkMp3s(AUDIO_ROOT);
  console.log(`Matching ${localMp3s.length} local mp3s to WP URLs…`);

  const map = {};
  const missing = [];
  for (const abs of localMp3s) {
    const fn = path.basename(abs);
    const key = wpSanitize(fn);
    const url = slugToUrl.get(key);
    if (url) {
      map[fn] = url;
    } else {
      missing.push(fn);
    }
  }

  if (missing.length) {
    console.warn(`\n  ${missing.length} local files NOT found on WP:`);
    for (const m of missing) console.warn('    -', m);
  }

  const header = [
    '/* AUTO-GENERATED by scripts/gen-wp-audio-map.mjs — do not edit by hand. */',
    '/* eslint-disable */',
    '',
    '// Maps local audio filename (basename, with extension) to the public',
    '// WordPress URL where the audio is actually hosted. WP flattens folder',
    '// structure on upload, so /Part0/1. Welcome.mp3 ends up at',
    '// /wp-content/uploads/YYYY/MM/1.-Welcome.mp3.',
    '',
    'export const WP_AUDIO_MAP: Record<string, string> = {',
  ].join('\n');
  const body = Object.keys(map)
    .sort()
    .map(k => `  ${JSON.stringify(k)}: ${JSON.stringify(map[k])},`)
    .join('\n');
  const footer = '\n};\n';

  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, header + '\n' + body + footer, 'utf8');
  const sizeKb = (fs.statSync(OUT_FILE).size / 1024).toFixed(1);
  console.log(`Wrote ${Object.keys(map).length} entries (${sizeKb} kB) → ${path.relative(ROOT, OUT_FILE)}`);
  if (missing.length) process.exit(1);
}

main().catch(e => { console.error(e); process.exit(1); });
